# 함수를 사용하여 두수를 입력받아, 더하기,빼기,곱하기,나누기 결과값을 출력하시요

def cal(s_list):
    for i in range(s_list[0],s_list[1]+1):
        s_list[2]+=i #더하기
        s_list[3]-=i #빼기
        s_list[4]*=i #곱하기

# 두수이력을 받아 리턴을 받아 출력

num1=int(input("1번째 숫자를 입력하시요>>>"))
num2=int(input("2번째 숫자를 입력하시요>>>"))
s_list=[num1,num2,0,0,1]
cal(s_list)
print("더하기 결과값:",s_list[2])
print("빼기 결과값:",s_list[3])
print("곱하기 결과값:",s_list[4])