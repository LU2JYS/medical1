# 관계연산자
#어떤것이 작거나 크거나를 비교
# 참은true 거짓은 false
# 주로 조거문 , 반목문에서 사용
# 수식1 == 수식2 : 수식1과 수식2가 같은지 평가
# 수식1 != 수식2 : 수식1과 수식2가 같지 않음을 평가
# 수식1 > 수식2 : 수식1이 수식2값보다 큰가를 평가 
# 수식1 >= 수식2 :수식1이 수식2값보다 큰거나 같거나 를 평가 
# <,<= 작은가,작거나 같은가
#등호는 뒤에 사용
print(3!= 6)

num=95
print(num>90)
print(num<=90)
print(num==90)
print(num!=90)

print(90<= num <100)

#논리연산자 >>>>>>>>>>>>>>꼭 기억하기
# and (그리고), or(또는), not(부정) 세가지 종류
# and 둘다 참이어야 함
# or 둘중 하나라도 참이면 참
# not 참이면 거짓 거짓이면 참

kor= 90
avg= 80
print(kor>80)
print(avg>90)
print(avg>90 and kor>80) #>>>하나라도 거짓이면 거짓으로 출력이 된다.
print(avg>90 or kor>80) #>>>하나라도 참이면 참으로 출력이 된다.
print(not kor>=80) #참인데 반대인 거짓으로 출력